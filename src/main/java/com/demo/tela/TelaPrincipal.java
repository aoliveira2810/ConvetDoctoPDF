    public void selecionaCamino(JTextField field){
        
            JFileChooser file = new JFileChooser(); 
            //file.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); 
            
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Tipos: docx,doc,pdf","docx", "doc", "pdf"); //tipo de arquivo
            file.addChoosableFileFilter(filter);
            file.setFileSelectionMode(JFileChooser.FILES_ONLY);
            int i= file.showSaveDialog(null);
          if (i==1){
              String dir = System.getProperty("user.home");
              field.setText(field.getText());
          } else {
              File arquivo = file.getSelectedFile();
              field.setText(arquivo.getPath());
            // JtextFieldLocal.setText(arquivo.getPath()+".txt");
          }
    }
    
 
  

