    private void botaoDocxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDocxActionPerformed
        selecionaCamino(jTextFieldArquivo);
        String ext = FilenameUtils.getExtension(jTextFieldArquivo.getText());
        if ("docx".equalsIgnoreCase(ext)) {
            botaoConverterDoctoPDF.setEnabled(true);
         } else if ("pdf".equalsIgnoreCase(ext)) { 
            botaoConverterPDFtoDoc.setEnabled(true);
        }else{
           JOptionPane.showMessageDialog(null, "TIPO DE ARQUIVO INVÁLIDO. SOMENTE .PDF e .docx são permitidos.");
           jTextFieldArquivo.setText("");
         } 
        
        
    }//GEN-LAST:event_botaoDocxActionPerformed

